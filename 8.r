# Calculate the natural logarithm of 10
ln_10 <- log(10)

# Display the result
cat("Natural Logarithm of 10:", ln_10, "\n")
# Calculate the absolute value
abs_value <- abs(-7)

# Display the result
cat("Absolute Value of -7:", abs_value, "\n")
# Round to the nearest integer
rounded_value <- round(3.75)

# Display the result
cat("Rounded Value of 3.75:", rounded_value, "\n")
# Calculate the square root
sqrt_value <- sqrt(25)

# Display the result
cat("Square Root of 25:", sqrt_value, "\n")
# Generate a random integer between 1 and 10
random_integer <- sample(1:10, 1)

# Display the random integer
cat("Random Integer between 1 and 10:", random_integer, "\n")



